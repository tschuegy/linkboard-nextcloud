// ─── Languages that get English source (identity mapping) ────────────
const identityLanguages = ['en_GB']

// ─── All target locales ─────────────────────────────────────────────
const allLocales = Object.keys(pluralForms)

// ─── Generate files ─────────────────────────────────────────────────
mkdirSync(L10N_DIR, { recursive: true })

let generatedCount = 0

for (const locale of allLocales) {
	let trans = {}

	if (identityLanguages.includes(locale)) {
		// Identity mapping - English strings map to themselves
		for (const s of sourceStrings) {
			trans[s] = s
		}
	} else if (langAliases[locale] && translations[langAliases[locale]]) {
		// Use alias base language
		trans = { ...translations[langAliases[locale]] }
	} else if (translations[locale]) {
		trans = { ...translations[locale] }
	} else {
		// Fallback: use English strings as-is (untranslated)
		for (const s of sourceStrings) {
			trans[s] = s
		}
	}

	const pf = pluralForms[locale]

	// Write JSON
	const jsonData = {
		translations: trans,
		pluralForm: pf,
	}
	writeFileSync(
		join(L10N_DIR, locale + '.json'),
		JSON.stringify(jsonData, null, 2) + '\n',
	)

	// Write JS
	const jsTransStr = JSON.stringify(trans, null, 4)
	const jsContent = `OC.L10N.register("linkboard", ${jsTransStr}, "${pf}");\n`
	writeFileSync(join(L10N_DIR, locale + '.js'), jsContent)

	generatedCount += 2
}

console.log(`Generated ${generatedCount} files for ${allLocales.length} locales in l10n/`)
